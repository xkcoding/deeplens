import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";

const SIDEBAR_PLACEHOLDER = "/* __SIDEBAR_PLACEHOLDER__ */";

function buildVitePressConfig(projectName: string): string {
  return `import { defineConfig } from "vitepress";
import { withMermaid } from "vitepress-plugin-mermaid";

export default withMermaid(
  defineConfig({
    title: ${JSON.stringify(projectName)},
    description: "Generated by DeepLens",
    themeConfig: {
      sidebar: ${SIDEBAR_PLACEHOLDER},
      nav: [{ text: "Home", link: "/" }],
    },
  }),
);
`;
}

function buildThemeIndex(): string {
  return `import DefaultTheme from "vitepress/theme";
import "./custom.css";

export default {
  extends: DefaultTheme,
};
`;
}

