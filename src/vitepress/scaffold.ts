function buildPackageJson(projectName: string): string {
  return JSON.stringify(
    {
      name: `${projectName}-docs`,
      private: true,
      type: "module",
      scripts: {
        "dev": "vitepress dev",
        "build": "vitepress build",
        "preview": "vitepress preview",
      },
      devDependencies: {
        "mermaid": "11.4.1",
        "vitepress": "^1.6.3",
        "vitepress-plugin-mermaid": "^2.0.17",
      },
    },
    null,
    2,
  );
}

export async function scaffoldVitePress(
  outputDir: string,
  projectName: string,
): Promise<void> {
  const vitepressDir = path.join(outputDir, ".vitepress");
  const themeDir = path.join(vitepressDir, "theme");
  await mkdir(themeDir, { recursive: true });

  await writeFile(
    path.join(vitepressDir, "config.mts"),
    buildVitePressConfig(projectName),
    "utf-8",
  );

  await writeFile(
    path.join(themeDir, "index.ts"),
    buildThemeIndex(),
    "utf-8",
  );

  await writeFile(
    path.join(themeDir, "custom.css"),
    buildCustomCSS(),
    "utf-8",
  );

  await writeFile(
    path.join(outputDir, "package.json"),
    buildPackageJson(projectName),
    "utf-8",
  );
}

